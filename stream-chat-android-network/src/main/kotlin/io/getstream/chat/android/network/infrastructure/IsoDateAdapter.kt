package io.getstream.chat.android.network.infrastructure

import com.squareup.moshi.FromJson
import com.squareup.moshi.ToJson
import java.text.SimpleDateFormat
import java.util.Date
import java.util.Locale
import java.util.TimeZone

class IsoDateAdapter {

    private val format = SimpleDateFormat("yyyy-MM-dd'T'HH:mm:ss.SSS'Z'", Locale.US).apply {
        timeZone = TimeZone.getTimeZone("UTC")
    }

    @ToJson
    @Synchronized
    fun toJson(value: Date): String = format.format(value)

    @FromJson
    @Synchronized
    fun fromJson(value: String): Date = format.parse(value)
}
